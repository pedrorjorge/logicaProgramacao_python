nota = int(input("Digite a nota no sistena numérico: "))

if nota == 0:
    print("Sua nota convertida é: E!")
elif nota >= 1 and nota <=35:
    print("Sua nota convertida é: D!")
elif nota >= 36 and nota <=60:
    print("Sua nota convertida é: C!")
elif nota >= 61 and nota <=85:
    print("Sua nota convertida é: B!")
elif nota >= 85 and nota <=100:
    print("Sua nota convertida é: A!")
