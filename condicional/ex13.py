quantidadeLivro = int(input("Digite a quantidade de livros: "))

criterioA = (0.25 * quantidadeLivro) + 7.50
criterioB = (0.50 * quantidadeLivro) + 2.50

print(f"Critério A: {criterioA}")
print(f"Critério B: {criterioB}")

if criterioA < criterioB:
    print("O valor do critério A é a melhor opção!")
else:
    print("O valor do critério A é a melhor opção!")
